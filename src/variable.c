#include <stdio.h>
/**
 * C 语言中最小存储单位是字节(Byte)，char 型变量占一个字节。
 */

int main(void)
{
    char firstletter;
    int hour, minute;
    int i = 0;

    firstletter = 'a';
    hour = 11;
    minute = 59;

    printf("Current time is %d:%d\n", hour, minute);

    printf("%d + %d = %d\n", i, i, i);

    return 0;
}
